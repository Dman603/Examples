### Problem statement at this link: https://projecteuler.net/problem=69

### The answer is the highest number under a million that you get from multiplying consecutive primes: 2x3x5x7x11x13x17 = 510510
### this is because any multiples of these numbers are not relatively prime, so for 2 reasons these will minimize phi(n). The first
### is that these primes are the lowest numbers possible, so it lets you multiply the most together. The second reason is that
### because these primes are small, they also have more multiples under n compared to a larger prime (ie. 2 has 510510//2 multiples
### but 17 only has 510510//17 multiples). 
